                        <h1>TRAVELLERS INFO FOR CAMBODIA</h1>
                        <h2>CLIMATE</h2>
                        <p>
                            Cambodia has four seasons: (i) Nov-Feb: cool & dry, (ii) Mar-May: hot and dry, (iii)
                            Jun-Aug: hot &
                            wet and (iv) Sep-Oct: cool & wet. Fortunately when raining it is very rare for it to last
                            the whole
                            day and it rarely impacts on tours. Most showers are short and heavy and the rest of the day
                            can be
                            optimum weather for touring with emerald green rice paddies and glorious sunsets at their
                            best.
                        </p>
                        <h2>CLOTHING</h2>
                        <p>
                            Definitely light and easy to wear clothes in natural fabrics are the best suggestion to suit
                            the
                            warm Cambodian climate. Cambodia is a country more designed for dressing down than up.
                            Though please
                            be respectful and keep beach attire to the beach. Short shorts and miniskirts are not
                            appreciated by
                            the conservative Cambodians.
                            When entering a place of worship such as an active temple then shoes and socks must be
                            removed.

                        </p>

                        <h2>CUISINE</h2>
                        <p>
                            Fresh and tasty – and not to be confused with the neighbouring countries of Thailand or
                            Vietnam.
                            Travel Asia a la carte is happy to provide visitors with a list of recommended restaurants.
                            The
                            international food scene is developing and excellent European and other Asian cuisines are
                            readily
                            obtainable in Phnom Penh and Siem Reap. The choice is more limited away from these areas
                            with the
                            exception of Kep where delicious seafood is a highlight of any visit.
                        </p>

                        <h2>ELECTRICITY</h2>
                        <p>
                            230V / 50Hz. Two-pin plugs are in use (both flat and round). Many hotels offer different
                            electrical
                            sockets and three-pin plugs will occasionally fit. It’s a good idea to bring a universal
                            plug
                            adaptor as hotels do not always have in good supply.
                        </p>

                        <h2>ETIQUETTE</h2>
                        <p>
                            Whole books have been written on culture in Cambodia. Your guide will be able to give an
                            explanation
                            of do’s and don’ts. Our one request is quite simple – to respect the religion but not
                            dressing in
                            short clothes at temples. Shoulders should be covered and shorts should reach the knee in
                            order to
                            be respectful.
                        </p>


                        <h2>FAMILY TRAVEL</h2>
                        <p>
                            As Cambodians are very family orientated there are always pleased to see foreign families
                            travelling
                            in the country who are made to feel most welcome. Whilst there can be a culture shock
                            children are
                            usually more readable able to adapt than children. Should car seats be required for young
                            children
                            please let us know in advance.
                        </p>

                        <h2>FESTIVALS</h2>
                        <p>The main festival periods are Khmer New Year, Pchum Ben and Water Festival. See Public
                            Holidays for
                            details.
                            Khmer New Year is a fun time to travel in Cambodia but does not see rampant water throwing
                            as in
                            Thailand. Angkor Wat and over temples may see a lot of domestic travelers.<br>
                            Pchum Ben is a spiritual experience and travelling and a visit to a temple over the 15 day
                            duration
                            is an incredible experience.<br>
                            The Water Festival brings crowds of locals to Phnom Penh. A highlight is boat racing on the
                            river
                            which can also be seen in Siem Reap on a smaller scale.<br>
                            Chinese New Year (Feb 2016/late Jan 2017) is a not an official holiday but can see very
                            large
                            numbers of travelers to Siem Reap at that time. Travel Asia a la carte recommends to avoid
                            travel to
                            Siem Reap over Chinese New Year to beat the crowds.
                        </p>

                        <h2>HEALTH</h2>
                        <p>Please consult your GP for detailed information. In our experience most travelers rarely
                            experience
                            health problems. If not used to the heat then take it easy especially for the first few
                            days. Drink
                            plenty of water and take breaks as required. Stomach upsets are possible as in all countries
                            so
                            bring some basic medicine even thought this can be easily purchased.<br>
                            In case of sickness most hotels can call a doctor. Local clinics and chemists can prescribe
                            medicines and usually understand good English. In the case of emergency then reasonable
                            hospitals
                            are available in Siem Reap and Phnom Penh. However emergency cases are often evacuated to
                            Bangkok.
                            <br>
                            In all cases it is the duty of the tourist to ensure for their own piece of mind (and
                            financially)
                            they have adequate insurance cover which should include medical evacuation.
                        </p>

                        <h2>INTERNET</h2>
                        <p>Access is readily available and is free at most hotels and many tourist restaurants. The
                            speed will
                            vary however. </p>

                        <h2>LANGUAGE</h2>
                        <p>The national language of Cambodia is Khmer. Attempting a few words in the local language will
                            win you
                            smiling approval from Cambodians. However it is not necessary for tourists to learn in
                            advance.
                            Staff in hotels and restaurants often speak good or fair English and of course local
                            transport
                            providers such as tuk tuk drivers will also usually understand. The French language is
                            spoken by a
                            few older people and younger students who learn it at school.</p>


                        <h2>MOBILE PHONES</h2>
                        <p>It is quite easy to obtain a local SIM card in Cambodia and use in your phone. The cost is
                            minimal
                            and a guide can help in making a stop at a shop to make this arrangement. A passport copy
                            and a
                            couple of passport photos may be required. 3G coverage is good or reasonable throughout most
                            of the
                            country. </p>

                        <h2>MONEY</h2>
                        <p>The Cambodian currency is called Riel and the exchange rate to the US dollar is around 4,000
                            Riel
                            (KHR). Interesting the currency is notes only with no coins. However we do not suggest
                            changing any
                            money into riel. Instead use US dollars throughout which are accepted throughout the whole
                            country.
                            Sometimes change is given as a combination of riel and dollar. When less than $1 change will
                            be in
                            Riel as US coins are not accepted. It is best to have lower denomination notes where
                            possible as
                            outside of hotels and restaurants it may be difficult to change $50 or $100 notes. In some
                            areas
                            bordering or close to Thailand, baht may be accepted at fair rates.<br>
                            Credit cards are accepted at large hotels and at some boutique hotels and restaurants. It is
                            best to
                            check in advance if you plan to pay with a card.
                            ATM machines (Visa, Cirrus, Mastercard) can be found in the large cities and increasingly in
                            smaller
                            towns. They distribute US dollars ensuring you don’t need to carry large amounts of cash for
                            the
                            duration of your trip.<br>
                            We do not suggest travellers cheques as only the Acleda Bank readily exchange these.
                        </p>

                        <h2>ORPHANAGES AND POVERTY</h2>
                        <p>Cambodia is still a very poor country and support is required for development. However given
                            money to
                            beggars or children selling souvenirs is not a good way of supporting them. Likewise visits
                            to
                            orphanages are strongly discouraged – many are not real orphanages and a professionally run
                            centre
                            would not allow unvetted visitors to visit vulnerable children. We support the Friends
                            International, ‘Say No to Orphanage Tourism’ campaign. <br>
                            Where possible Travel Asia a la carte seeks to include responsible tourism practices into
                            our
                            programmes. This can be the visit of a training restaurant for poor children or visiting
                            Phare
                            Circus which has helped provide a career and a focus for many children. We find these far
                            better
                            examples of Responsible Tourism than a short term donation which may provide one meal but
                            does not
                            help to alleviate the causes of poverty. However we appreciate that when travelling in
                            poorer
                            countries it is not always easy to make decisions on when to give money or not.
                        </p>

                        <h2>PHOTOGRAPHY</h2>
                        <p>Don’t forget your camera as Cambodia is a very photogenic country! As in most countries you
                            should
                            ask if it is ok to take a photo of someone. Nearly always the answer is positive and rarely
                            does
                            anyone ask for money as a result. Your guide can ask in the Khmer language if it is ok to
                            take a
                            picture or you can show the camera and see if the person nods his or her head in approval.
                            <br>
                            If you need more memory cards we suggest to buy from a professional camera shop and not from
                            a
                            street seller – fake cards are often sold at discount prices.

                        </p>

