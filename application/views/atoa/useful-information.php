                        <h2>SAFETY</h2>
                        <p>Cambodia is a safe country for travelers. The biggest danger is traffic and bad driving! (be
                            careful
                            crossing the road in the cities!). Crime is overall low and against tourists is uncommon.
                            Pickpointing and snatch theft of bags does happen in Phnom Penh so we advise to be careful
                            of
                            valuables and leave money in hotel safety boxes unless required. However when comparing the
                            level of
                            safety in Cambodia, it is far better than the majority of large or medium size European and
                            American
                            cities. <br>
                            We do get asked about landmines and whether they still exist – they do but the last majority
                            have
                            been cleared and those remaining are not close to tourists areas. Even on an adventurous
                            trekking
                            holiday there would be no crossing of anywhere with mines. Seriously, this is of no concern
                            to
                            tourists though sadly some villagers have to take low level risks in fields each day.
                        </p>

                        <h2>SHOPPING</h2>
                        <p>In the areas mostly frequently visited by tourists there is a wide range of shops from
                            selling simple
                            souvenirs up to world class Khmer designer clothes. It is fair to say Cambodia is not the
                            equivalent
                            of Bangkok or Singapore so the selection is not as varied. Popular souvenirs include
                            traditional
                            scarves (krama), dried Kampot pepper, silver and bronze work. Travel Asia a la carte is
                            happy to
                            suggest a list of shopping venues to meet your needs. </p>

                        <h2>TIME</h2>
                        <p>GMT + 7.<br>
                            Sunrise and sunset both come relatively early.</p>

                        <h2>TIPPING</h2>
                        <p>It is never compulsory but always appreciated in a country where salaries are low. For tour
                            guides
                            and drivers it does help supplement their regulated salaries but should only be given when
                            good
                            service is provided. </p>

                        <h2>VACCINATIONS</h2>
                        <p>No vaccinations are required, except for Yellow Fever if you are arriving from a country
                            where the
                            disease is present.<br>
                            Malaria is not found in Siem Reap or Phnom Penh but does exist in some other parts of the
                            country.<br>
                            We recommend to consult your GP before travel for their recommendations. Also a
                            comprehensive travel
                            insurance is recommended.
                        </p>

                        <h2>VISAS</h2>
                        <p>Visas can be obtained in your home country in some cases or also on arrival for nearly all
                            nationalities. An online e-Visa service is also available. Most nationalities can obtain a
                            visa on
                            arrival – tourist visas are valid for 30 days, cost $30 and 1 passport photo is required.
                            The queue
                            on arrival is usually not long (5-15 minutes) though delays can be experienced when more
                            than one
                            flight arrives at the same time. The facility is available at the two international airports
                            in Siem
                            Reap and Phnom Penh and at all land borders. Please ensure your passport has 6 months
                            validity after
                            the date of departure from Cambodia and enough space for the visa and stamps (2 pages).</p>

                        <h2>WOMEN TRAVELLERS</h2>
                        <p>There is no problem with women to travel by themselves in Cambodia. Like in all countries we
                            recommend female travelers to be aware of their surroundings and ask guides advice on going
                            out at
                            night. </p>


